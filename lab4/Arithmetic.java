

public class Arithmetic {
    public static void main(String[] args) {
        System.out.println("The absolute value of -308 is " + Math.abs(-308));
        System.out.println("The ceiling of 2.13 is " + Math.ceil(2.13));
        System.out.println("The floor of 2.13 is " + Math.floor(2.13));;
        System.out.println("The maximum of (45, 207) returns " + Math.max(45, 207));
        System.out.println("The minimum of (3.8, 2.75) returns " + Math.min(3.8, 2.75));
        System.out.println("The 4 power of 3 is " + Math.pow(3,4));
        System.out.println("The round value of 2.718 is " + Math.round(2.718));
        System.out.println("The square root of 81 is " + Math.sqrt(81));
    }
}
